package com.pandero.ws.bean;

public class PedidoInversionCaspio {
	
	private String InversionId;
	private String PedidoId;
	private String TipoInversion;
	private String ImporteInversion;
	private String TipoInmueble;
	
	public String getInversionId() {
		return InversionId;
	}
	public void setInversionId(String inversionId) {
		InversionId = inversionId;
	}
	public String getPedidoId() {
		return PedidoId;
	}
	public void setPedidoId(String pedidoId) {
		PedidoId = pedidoId;
	}
	public String getTipoInversion() {
		return TipoInversion;
	}
	public void setTipoInversion(String tipoInversion) {
		TipoInversion = tipoInversion;
	}
	public String getImporteInversion() {
		return ImporteInversion;
	}
	public void setImporteInversion(String importeInversion) {
		ImporteInversion = importeInversion;
	}
	public String getTipoInmueble() {
		return TipoInmueble;
	}
	public void setTipoInmueble(String tipoInmueble) {
		TipoInmueble = tipoInmueble;
	}
	
	
	
}
